



function burger(){

    let fast = document.getElementById('burger_menu_ul');

    if(fast.style.visibility == 'hidden'){
        fast.style.visibility="visible";
    }else{
        fast.style.visibility='hidden'
    }
}

function dropdown(){
    let loww = document.getElementById('dropdown_items');

    if(loww.style.visibility == 'hidden'){
        loww.style.visibility="visible";
    }else{
        loww.style.visibility='hidden'
    }
}




